#include <iostream>
using namespace std;
int main()
{
	int a=2; //integer data type
	double const b=4; //double data type
	float c=1.5f; //float with f 
	float e = 5.8; //float without f 
	char d='A'; //character data type
	char f[]="Star";
		cout <<"The value of a is" <<a <<"\n";
		cout <<"The value of b is" <<b <<"\n";
		cout <<"The value of c is" <<c <<"\n";
		cout <<"The value of d is" <<d <<"\n";
		cout <<"The value of e is" <<e <<"\n";
		cout <<"The value of f is" <<f <<"\n";
	return 0;
}
		
